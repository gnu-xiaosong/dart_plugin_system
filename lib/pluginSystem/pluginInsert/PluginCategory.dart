/*
公共插件分类类别枚举类型
 */

// 公共插件分类类别枚举类型
enum PluginCategory {
  Authentication, // A - 身份验证相关插件
  Backup, // B - 备份和恢复插件
  Communication, // C - 通信插件（例如聊天、视频通话）
  Database, // D - 数据库相关插件
  Encryption, // E - 加密插件
  FileManagement, // F - 文件管理插件
  Graphics, // G - 图形和图像处理插件
  Hosting, // H - 主机服务或托管插件
  Integration, // I - 第三方服务集成插件
  JobScheduler, // J - 作业调度插件
  KeyManagement, // K - 密钥管理插件
  Reporting, // R - 报告生成插件
  Monitoring, // M - 监控和日志记录插件
  Notification, // N - 通知系统插件
  Optimization, // O - 性能优化插件
  Payment, // P - 支付和计费插件
  QueryProcessing, // Qw - 查询处理插件
}
